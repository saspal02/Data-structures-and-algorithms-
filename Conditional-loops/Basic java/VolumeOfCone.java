// 14. Volume Of Cone Java Program
public class VolumeOfCone {
    public static void main(String[] args) {
        double radius = 5; // Change the value of radius as per your requirement
        double height = 10; // Change the value of height as per your requirement
        double volume = (1.0/3.0) * Math.PI * radius * radius * height;
        System.out.println("Volume of cone with radius " + radius + " and height " + height + " is: " + volume);
    }
}

// 16. Volume Of Cylinder
public class VolumeOfCylinder {
    public static void main(String[] args) {
        double radius = 5; // Change the value of radius as per your requirement
        double height = 10; // Change the value of height as per your requirement
        double volume = Math.PI * radius * radius * height;
        System.out.println("Volume of cylinder with radius " + radius + " and height " + height + " is: " + volume);
    }
}
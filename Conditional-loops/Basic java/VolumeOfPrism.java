// 15. Volume Of Prism
public class VolumeOfPrism {
    public static void main(String[] args) {
        double base = 5; // Change the value of base as per your requirement
        double height = 10; // Change the value of height as per your requirement
        double depth = 3; // Change the value of depth as per your requirement
        double volume = base * height * depth;
        System.out.println("Volume of prism with base " + base + ", height " + height + " and depth " + depth + " is: " + volume);
    }
}
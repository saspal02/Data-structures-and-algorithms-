// 18. Volume Of Pyramid
public class VolumeOfPyramid {
    public static void main(String[] args) {
        double base = 5; // Change the value of base as per your requirement
        double height = 10; // Change the value of height as per your requirement
        double volume = (1.0/3.0) * base * height;
        System.out.println("Volume of pyramid with base " + base + " and height " + height + " is: " + volume);
    }
}